from collections import deque

def minimumMoves(grid, startX, startY, goalX, goalY):
    
    moves_required = [[-1]*len(grid[0]) for _ in range(len(grid))]
    moves_required[startX][startY] = 0
    
    to_explore = deque()
    to_explore.append((startX, startY, 0))
    
    while to_explore:
        
        x, y, points = to_explore.pop()
        
        next_points = points + 1
        
        #Explore up
        dx = 1
        while x - dx >= 0 and grid[x - dx][y] != 'X':
            if moves_required[x - dx][y] == -1:
                moves_required[x - dx][y] = next_points
                to_explore.appendleft((x - dx, y, next_points))
            dx += 1
        
        #Explore down
        dx = 1
        while x + dx < len(grid) and grid[x + dx][y] != 'X':
            if moves_required[x + dx][y] == -1:
                moves_required[x + dx][y] = next_points
                to_explore.appendleft((x + dx, y, next_points))
            dx += 1
            
        #Explore right
        dy = 1
        while y - dy >= 0 and grid[x][y - dy] != 'X':
            if moves_required[x][y - dy] == -1:
                moves_required[x][y - dy] = next_points
                to_explore.appendleft((x, y - dy, next_points))
            dy += 1
            
        #Explore left
        dy = 1
        while y + dy < len(grid[0]) and grid[x][y + dy] != 'X':
            if moves_required[x][y + dy] == -1:
                moves_required[x][y + dy] = next_points
                to_explore.appendleft((x, y + dy, next_points))
            dy += 1
            
    return moves_required[goalX][goalY]
    
